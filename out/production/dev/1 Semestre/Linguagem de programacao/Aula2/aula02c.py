# Operadores Aritméticos
# +,-,*,/,**(potência), % (módulo - resto da divisão inteira), //(Quociente da divisão inteira).

# Declaração de variáveis
a = 4
b = 2

# Processos
print("Soma:" ,a+b)
print("Subtração:" ,a-b)
print("Multiplicação:" ,a*b)
print("Divisão(/):" ,a/b)
print("Quociente da divisão inteira(//):" ,a//b)
print("Potência:" ,a**b)
print("Módulo (Resto da divisão):" ,a%b)
